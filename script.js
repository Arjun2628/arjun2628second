function btnblack(a ,b){
    document.getElementById(""+a+"").style.backgroundColor='rgb(0, 0, 0)'
    document.getElementById(""+b+"").style.color='rgb(255, 255, 255)'

}
function btnwhite(a,b){
    document.getElementById(""+a+"").style.backgroundColor=null
    document.getElementById(""+b+"").style.color='rgb(0, 0, 0)'

}


 